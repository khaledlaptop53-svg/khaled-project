def view_catalog(catalog: dict[str, Course]):
    print(f"\n  {'─'*58}")
    print(f"  {'AVAILABLE COURSES THIS SEMESTER':^56}")
    print(f"  {'─'*58}")
    for course in catalog.values():
        course.display_course_info()
        print()

def register_course(catalog: dict[str, Course], schedule: StudentSchedule):
    print("\n  Enter the Course ID to register (e.g. CS101): ", end="")
    code = input().strip().upper()
    if code not in catalog:
        print(f"\n  ✗ Course '{code}' not found in the catalog.")
        return
    if schedule.is_registered(code):
        print(f"\n  ✗ You are already registered for {catalog[code].name}.")
        return
    schedule.add_course(catalog[code])
    print(f"\n  ✓ Successfully registered for: {catalog[code].name}")

def drop_course(catalog: dict[str, Course], schedule: StudentSchedule):
    print("\n  Enter the Course ID to drop: ", end="")
    code = input().strip().upper()
    if schedule.remove_course(code):
        print(f"\n  ✓ '{code}' has been removed from your schedule.")
    else:
        print(f"\n  ✗ You are not registered for '{code}'.")

def get_student_name() -> str:
    while True:
        print("\n  Please enter your full name to begin: ", end="")
        name = input().strip()
        if name:
            return name
        print("  Name cannot be empty. Please try again.")

