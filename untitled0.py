from abc import ABC, abstractmethod

class Course(ABC):

    def __init__(self, course_id: str, name: str, credit_hours: int, cost_per_credit: float):
        self._course_id = course_id
        self._name = name
        self.__credit_hours = credit_hours
        self.__cost_per_credit = cost_per_credit

    @property
    def course_id(self):
        return self._course_id

    @property
    def name(self):
        return self._name

    @property
    def credit_hours(self):
        return self.__credit_hours

    @credit_hours.setter
    def credit_hours(self, value: int):
        if not isinstance(value, int) or value < 1 or value > 6:
            raise ValueError("Credit hours must be an integer between 1 and 6.")
        self.__credit_hours = value

    @property
    def cost_per_credit(self):
        return self.__cost_per_credit

    @cost_per_credit.setter
    def cost_per_credit(self, value: float):
        if value <= 0:
            raise ValueError("Cost per credit must be a positive number.")
        self.__cost_per_credit = value

    def base_tuition(self) -> float:
        return self.__credit_hours * self.__cost_per_credit

    @abstractmethod
    def calculate_tuition(self) -> float:
        pass

    @abstractmethod
    def display_course_info(self):
        pass


class PracticalLab(Course):

    EQUIPMENT_FEE = 150.00

    def __init__(self, course_id: str, name: str, credit_hours: int,
                 cost_per_credit: float, safety_gear_required: bool = False):
        super().__init__(course_id, name, credit_hours, cost_per_credit)
        self.safety_gear_required = safety_gear_required

    def calculate_tuition(self) -> float:
        return self.base_tuition() + self.EQUIPMENT_FEE

    def display_course_info(self):
        gear = "Yes" if self.safety_gear_required else "No"
        print(f"  [{self.course_id}] {self.name}")
        print(f"        Type        : Practical Lab")
        print(f"        Credits     : {self.credit_hours} hrs  |  Base: ${self.base_tuition():.2f}")
        print(f"        Equipment   : +${self.EQUIPMENT_FEE:.2f} flat fee")
        print(f"        Safety Gear : {gear}")
        print(f"        Total Cost  : ${self.calculate_tuition():.2f}")


class TheoreticalLecture(Course):

    TECH_FEE_RATE = 0.05

    def __init__(self, course_id: str, name: str, credit_hours: int,
                 cost_per_credit: float, hall_capacity: int = 200,
                 online_link: str = ""):
        super().__init__(course_id, name, credit_hours, cost_per_credit)
        self._hall_capacity = hall_capacity
        self.online_link = online_link

    @property
    def hall_capacity(self):
        return self._hall_capacity

    @hall_capacity.setter
    def hall_capacity(self, value: int):
        if not isinstance(value, int) or value < 1:
            raise ValueError("Hall capacity must be a positive integer.")
        self._hall_capacity = value

    def calculate_tuition(self) -> float:
        tech_fee = self.base_tuition() * self.TECH_FEE_RATE
        return self.base_tuition() + tech_fee

    def display_course_info(self):
        tech_fee = self.base_tuition() * self.TECH_FEE_RATE
        link_display = self.online_link if self.online_link else "N/A"
        print(f"  [{self.course_id}] {self.name}")
        print(f"        Type        : Theoretical Lecture")
        print(f"        Credits     : {self.credit_hours} hrs  |  Base: ${self.base_tuition():.2f}")
        print(f"        Tech Fee    : +${tech_fee:.2f} (5% of base)")
        print(f"        Hall Cap.   : {self.hall_capacity} students")
        print(f"        Online Link : {link_display}")
        print(f"        Total Cost  : ${self.calculate_tuition():.2f}")


class StudentSchedule:

    def __init__(self, student_name: str):
        self.student_name = student_name
        self._courses: list[Course] = []

    def add_course(self, course: Course) -> bool:
        if any(c.course_id == course.course_id for c in self._courses):
            return False
        self._courses.append(course)
        return True

    def remove_course(self, course_id: str) -> bool:
        for c in self._courses:
            if c.course_id == course_id:
                self._courses.remove(c)
                return True
        return False

    def is_registered(self, course_id: str) -> bool:
        return any(c.course_id == course_id for c in self._courses)

    def view_schedule(self):
        if not self._courses:
            print("\n  Your schedule is empty.")
            return
        print(f"\n  {'─'*52}")
        print(f"  Registered Courses for: {self.student_name}")
        print(f"  {'─'*52}")
        for course in self._courses:
            print(f"  • [{course.course_id}] {course.name}  ({course.credit_hours} credits)")
        print(f"  {'─'*52}")

    def print_tuition_bill(self):
        if not self._courses:
            print("\n  No courses registered. Nothing to bill.")
            return

        divider = "═" * 58
        thin = "─" * 58

        print(f"\n  {divider}")
        print(f"  {'CAIRO TECH UNIVERSITY':^56}")
        print(f"  {'SEMESTER TUITION BILL':^56}")
        print(f"  {divider}")
        print(f"  Student : {self.student_name}")
        print(f"  {thin}")
        print(f"  {'COURSE':<30} {'CREDITS':>7} {'TOTAL':>14}")
        print(f"  {thin}")

        grand_total = 0.0
        for course in self._courses:
            cost = course.calculate_tuition()
            grand_total += cost
            label = course.name[:28]
            print(f"  {label:<30} {course.credit_hours:>5} cr   ${cost:>10.2f}")

        print(f"  {thin}")
        print(f"  {'GRAND TOTAL':<38} ${grand_total:>14.2f}")
        print(f"  {divider}")
        print(f"  * Lab courses include a $150.00 equipment fee.")
        print(f"  * Lecture courses include a 5% technology fee.")
        print(f"  {divider}\n")


